import java.util.Scanner;
public class Star05 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        System.out.print("Masukan jumlah *: ");
        int n = sc.nextInt();

        for (int i = 0; i <= n; i--) {
            System.out.print("*");
        }
    
}
    
}
